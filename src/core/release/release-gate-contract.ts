export function releaseGateContractSnapshot() {
  const ids = [...RELEASE_GATE_CONTRACT_IDS]
  const sha256 = crypto
    .createHash('sha256')
    .update(`${RELEASE_GATE_CONTRACT_SCHEMA}\n${ids.join('\n')}\n`)
    .digest('hex')
  return {
    schema: RELEASE_GATE_CONTRACT_SCHEMA,
    ids,
    count: ids.length,
    sha256
  }
}
